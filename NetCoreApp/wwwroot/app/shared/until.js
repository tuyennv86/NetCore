    unflattern: function (arr) {
        let map = {};
        let roots = [];
        for (let i = 0; i < arr.length; i += 1) {
            let node = arr[i];
            node.children = [];
            map[node.id] = i; // use map to look-up the parents
            if (node.parentId !== null) {
                arr[map[node.parentId]].children.push(node);
            } else {
                roots.push(node);
            }
        }
        return roots;
    },
